package Chapter.Chapter_04.Checkpoin_04;

public class CP12 {
	public static void main(String[] args) {
		
		
		char c = 'A';
		int i = (int)c;
		System.out.println(i);
		
		float f = 1000.34f;
		int a = (int)f;
		System.out.println(a);
		
		double d = 1000.34;
		int e = (int)d;
		System.out.println(e);
		
		int g = 97;
		char l = (char)g;
		System.out.println(l);
	}
}
